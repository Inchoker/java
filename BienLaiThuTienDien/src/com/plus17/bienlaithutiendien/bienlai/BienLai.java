package com.plus17.bienlaithutiendien.bienlai;

public class BienLai {
	private String hoTenChuHo, soNha, maCongTo, maBienLai, chiSoCu, chiSoMoi;
	private int soTienPhaiTra;

	public BienLai(String hoTenChuHo, String soNha, String maCongTo, String maBienLai, String chiSoCu, String chiSoMoi,
			int soTienPhaiTra) {
		super();
		this.hoTenChuHo = hoTenChuHo;
		this.soNha = soNha;
		this.maCongTo = maCongTo;
		this.maBienLai = maBienLai;
		this.chiSoCu = chiSoCu;
		this.chiSoMoi = chiSoMoi;
		this.soTienPhaiTra = soTienPhaiTra;
	}

}
